#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*rtn;

	rtn = malloc(size * count);
	if (rtn == NULL)
		return (NULL);
	ft_memset(rtn, 0, size * count);
	return (rtn);
}
