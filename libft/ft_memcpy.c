#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	unsigned char	*d;
	unsigned char	*s;
	size_t			i;

	d = (unsigned char *)dest;
	s = (unsigned char *)src;
	i = n;
	if (d == 0 && s == 0)
		return (NULL);
	while (i--)
		*d++ = *s++;
	return (dest);
}
